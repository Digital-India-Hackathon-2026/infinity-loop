# JWT Config
SECRET_KEY = "FARMER2GOV_SECRET_SUPER_KEY_FOR_HACKATHON"
ALGORITHM = "HS256"
ACCESS_TOKEN_EXPIRE_MINUTES = 60 * 24  # 1 day for hackathon demo

oauth2_scheme = OAuth2PasswordBearer(tokenUrl="/api/auth/login", auto_error=False)

